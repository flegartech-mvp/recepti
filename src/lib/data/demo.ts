const byId = (id: string) => demoIngredients.find((item) => item.id === id)!;

function recipeIngredient(
  id: string,
  ingredientId: string,
  quantity: number | null,
  unit: string | null,
  sortOrder: number,
  optional = false,
) {
  const item = byId(ingredientId);
  return {
    id,
    ingredientId,
    canonicalName: item.canonicalName,
    displayName: item.displayName,
    normalizedName: item.normalizedName,
    quantity,
    unit,
    preparationNote: null,
    isOptional: optional,
    isGarnish: false,
    sectionName: null,
    sortOrder,
  };
}

