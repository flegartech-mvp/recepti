const ingredient = (
  id: string,
  name: string,
  category: Ingredient["category"],
  defaultUnit: string | null,
  isStaple = false,
): Ingredient => ({
  id,
  canonicalName: name,
  displayName: name,
  normalizedName: name.toLocaleLowerCase("en-US"),
  category,
  defaultUnit,
  aliases: [],
  isStaple,
  notes: null,
});

